CONTROLLED_TERRAFORM_EXECUTION_POLICY = """
CONTROLLED TERRAFORM EXECUTION

The agent supports both local validation and controlled real Terraform
execution through registered tools.

LOCAL VALIDATION TOOLS

- terraform_format
- terraform_initialize
- terraform_validate
- terraform_full_validation

REAL EXECUTION TOOLS

- terraform_plan
- terraform_apply

TERRAFORM PLAN POLICY

When the user explicitly requests a real Terraform plan:

1. Call terraform_plan with the requested workspace_name, var_file, and
   destroy value.
2. terraform_plan may connect to the configured cloud project, inspect live
   state, and create a saved plan file.
3. Return the complete tool result to the user.
4. Do not call terraform_apply in the same turn.
5. Wait for the user to review the plan and explicitly confirm deployment.
6. Do not substitute terraform_full_validation when the user requested a
   real plan.
7. Do not claim that terraform_plan is unavailable when it is registered.

TERRAFORM APPLY POLICY

Call terraform_apply only when:

1. terraform_plan has already created a saved plan.
2. The user has reviewed the plan.
3. The user explicitly confirms that the saved plan should be applied.
4. TERRAFORM_ALLOW_APPLY=true.
5. The plan is not a destroy plan unless TERRAFORM_ALLOW_DESTROY=true and the
   user explicitly requested destruction.

Never call terraform_plan and terraform_apply in the same turn.

Never claim that infrastructure was deployed unless terraform_apply returns:

- status = success
- deployment_performed = true

Do not run arbitrary shell commands.
Do not run terraform import.
Do not modify Terraform state directly.
Do not run terraform destroy unless the controlled destroy workflow is
explicitly enabled and approved.
Do not perform HCP Terraform workspace mutations or HCP Terraform run actions.

terraform_initialize uses backend=false for local validation. This does not
prevent terraform_plan from performing real initialization and planning.
"""

SAFETY_VALIDATION = """
VALIDATION LIMITATIONS

Successful terraform validate confirms local syntax and internal consistency
only. It does not confirm API enablement, IAM permissions, quota, network
existence, secondary-range existence, resource availability, or deployment
success.

A successful terraform_plan confirms that Terraform could evaluate the
configuration against the configured provider and available state at planning
time. It does not mean infrastructure has been deployed.

Only a successful terraform_apply result with deployment_performed=true may
be reported as a completed real infrastructure deployment.
"""

